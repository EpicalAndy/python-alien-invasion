separator = '~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~'

# Целые числа
a = 2
b = 3

print(f'{a} + {b} = {a + b}\n{a} * {b} = {a * b}\n{a} ** {b} = {a ** b}')

print(separator)

# Вещественные числа
a = 3.3
b = 4.7

print(f'{a} + {b} = {a + b}\n{a} * {b} = {a * b}\n{a} ** {b} = {a ** b}')

print(separator)

# При делении всегда получаем Вещественное число!

a = 6
b = 2

print(f'{a} / {b} = {a / b}')

print(separator)

# Можно вводить числа чере подчёркивание!
a = 10_000_000_234

print(a)
